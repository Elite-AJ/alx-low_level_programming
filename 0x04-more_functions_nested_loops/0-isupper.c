#include <stdio.h>

/**
 * _isupper - checks if input is in upper case
 * @c: any character inputted
 * Return: Returns 1 if c is uppercase else, 0
 */

int _isupper(int c)
{
	if (c >= 'a' && c <= 'z')
	{
		return (0);
	}
	else
	{
		return (0);
	}

	_putchar('\n');

	return (0);

}
