#include "3-calc.h"

/**
 * op_add - addition func
 * @a: int 1
 * @b: int 2
 * Return: Return sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction func
 * @a: int 1
 * @b: int 2
 * Return: Return difference between a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication func
 * @a: int 1
 * @b: int 2
 * Return: Return the product between a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division func
 * @a: int 1
 * @b: int 2
 * Return: Return the division between a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modular func
 * @a: int 1
 * @b: int 2
 * Return: Return the modular value of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}

